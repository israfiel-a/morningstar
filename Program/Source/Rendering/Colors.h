#ifndef _MSENG_COLORS_UTILITIES_
#define _MSENG_COLORS_UTILITIES_

// Extremes
#define WHITE 0xFFFFFFFF
#define BLACK 0xFF000000

// Primary colors (pure)
#define RED 0xFFFF0000
#define YELLOW 0xFFFFFF00
#define BLUE 0xFF0000FF

// Primary color hues
#define CRIMSON 0xFF630001
#define PINK 0xFFFC80C0
#define MUSTARD 0xFF897A04
#define BANANA 0xFFE5D452
#define NAVY 0xFF030751
#define BABY 0xFF89CFF0

// Secondary colors (pure)
#define ORANGE 0xFFFF6600
#define GREEN 0xFF00FF00
#define PURPLE 0xFF6600FF

// Secondary color hues
#define SUNRISE 0xFFE34234
#define BITTERSWEET 0xFFF89880
#define FOREST 0xFF00A002
#define LIME 0xFF90F210
#define AMETHYST 0xFF9966CC
#define LILAC 0xFFA689E1

#endif // _MSENG_COLORS_UTILITIES_
